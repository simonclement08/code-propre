package ex1;

public class Calcul {

	/**
	 * Permet de calculer la somme de deux entiers
	 * @param a
	 * @param b
	 * @return la somme
	 */
	public int addition(int a, int b){
		return a+b;
	}
	
	/**
	 * Permet de calculer la différence entre deux entiers
	 * @param a
	 * @param b
	 * @return la différence
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
